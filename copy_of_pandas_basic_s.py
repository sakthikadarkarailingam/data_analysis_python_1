import numpy as np
import pandas as pd
data = {
    "Name": ["santhanam", "Selvi", "pandi", "rama","Sam","pavi"],
    "Age": [25, 30, 22,67,89,78],
    "Salary": [5000, 6000, 4000, 9000,8000,2000]
}
df = pd.DataFrame(data)
print(df)
df[df['Salary'].max() == df['Salary']]

df[df['Salary'].max() == df['Salary']] # yengalam adhigama salary match agudho , andha person oda peru age salary yellame varum

df[df['Salary'].min() == df['Salary']] # yengalam kammiana salary match agudho , andha person oda peru age salary yellame varum

# oldest person yaro kandupudikalam
df[df['Age'].max() == df['Age']]

# youngest person yaro kandupudikalam
df[df['Age'].min() == df['Age']]

